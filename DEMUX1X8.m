function [out]=DEMUX1X8(in,s0,s1,s2)
s1not=NOT1(s1);
s0not=NOT1(s0);
s2not=NOT1(s2);
d0a=AND1(s0not, s1not);
d0b=AND1(d0a,s2not);
d0=AND1(d0b,in);
d1a=AND1(s1not, s0);
d1b=AND1(d1a,s2not);
d1=AND1(d1b,in);
d2a=AND1(s0not, s1);
d2b=AND1(d2a,s2not);
d2=AND1(d2b,in);
d3a=AND1(s0, s1);
d3b=AND1(d3a,s2not);
d3=AND1(d3b,in);
d4a=AND1(s1not, s0not);
d4b=AND1(d4a,s2);
d4=AND1(d4b,in);
d5a=AND1(s0, s1not);
d5b=AND1(d5a,s2);
d5=AND1(d5b,in);
d6a=AND1(s0not, s1);
d6b=AND1(d6a,s2);
d6=AND1(d6b,in);
d7a=AND1(s0, s1);
d7b=AND1(d7a,s2);
d7=AND1(d7b,in);
[out]=[ d0 d1 d2 d3 d4 d5 d6 d7];
end
